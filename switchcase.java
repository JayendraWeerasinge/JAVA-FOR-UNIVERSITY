public class switchcase{
    public static void main(String[] args){
        int age;
        age = 4;

        switch (age) {
            case 1:
                System.out.println("You are too small buddy");
                break;

            case 2:
                System.out.println("You are growing fast");
                break;

            case 3:
                System.out.println("You are about to get fucked up");
                break;
                
            default:
                break;
        }
    } 
}